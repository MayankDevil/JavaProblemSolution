class Test
{
    public static void main(String args[])
    {
        int n = 1;    // default number

        int num = 2;  // take number

        /* compare is equal | greater | less */

        if  (num > n)
            System.out.println(num+" greater than "+n);
        if  (num < n)
            System.out.println(num+" less than "+n);
        if  (num == n)
            System.out.println(num+" equal to "+n);

    }
}
